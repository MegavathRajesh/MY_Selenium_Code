package javabasis;

public class Datatypes {

	public static void main(String[] args) {
		
		byte a = -127;// byte range value -128 to 127
		System.out.println(a);
		
		short b = 23264; // short range value -32,768 to 32,767
		 
		System.out.println(b);
		
		int c = 547353723; // Int range value -2,147,483,648 to 2,147,483,647
		System.out.println(c);
		
		long d = 65357363L; // Long range value -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		System.out.println(d);
		
		float  e = 645363f;
		
		System.out.println(e);
		
		double f = 9;
		System.out.println("haribabu grade  "+f);
		
		char g = 'A'; // char store in single Quotes
		
		System.out.println(g);
		
		boolean  s = true;
		System.out.println(s);
	}
	

}
