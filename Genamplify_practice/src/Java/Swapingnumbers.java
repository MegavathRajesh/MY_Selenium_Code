package Java;

public class Swapingnumbers {

	public static void main(String[] args) {
            int a = 10;
            int b = 12;
         a=a+b;// if we add a+b we get value = 22
         b=a-b;// here the value of a-b = 10
          a=a-b;
         System.out.println(a);
         System.out.println(b);
       
	}

}
